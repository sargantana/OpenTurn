void f32SinCos(uint32_t index, float *sin_val, float *cos_val)
{

  index = index & (SINETABLE_STEPS_PER_PERIOD - 1);

  if (index >= (3*SINETABLE_STEPS_PER_PERIOD/4))
  {
      *sin_val = -f32_sinetable[SINETABLE_STEPS_PER_PERIOD - index];
      *cos_val = f32_sinetable[index - (3*SINETABLE_STEPS_PER_PERIOD/4)];
  }
  else if (index >= (SINETABLE_STEPS_PER_PERIOD/2))
  {
      *sin_val = -f32_sinetable[index - (SINETABLE_STEPS_PER_PERIOD/2)];
      *cos_val = -f32_sinetable[(3*SINETABLE_STEPS_PER_PERIOD/4) - index];
  }
  else if (index >= (SINETABLE_STEPS_PER_PERIOD/4))
  {
      *sin_val = f32_sinetable[(SINETABLE_STEPS_PER_PERIOD/2) - index];
      *cos_val = -f32_sinetable[index - (SINETABLE_STEPS_PER_PERIOD/4)];
  }
  else
  {
      *sin_val = f32_sinetable[index];
      *cos_val = f32_sinetable[(SINETABLE_STEPS_PER_PERIOD/4) - index];
  }

  return;
}
